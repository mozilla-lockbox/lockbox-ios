import Foundation

extension Data {
    func base64URLEncodedString() -> String {
        let encodedString = self.base64EncodedString()

        return encodedString
                .replacingOccurrences(of: "+", with: "-")
                .replacingOccurrences(of: "/", with: "_")
                .replacingOccurrences(of: "=", with: "")
    }
}